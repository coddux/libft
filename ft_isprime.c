#include "inc/libft.h"

int		ft_isprime(int p)
{
	int		i;

	i = 3;
	while (((!p % 2) && (p != 2)) || (p <= 1))
		return (0);
	while ((i * i) < p)
	{
		if ((p % i) == 0)
			return (0);
		i = i + 2;
	}
	return (1);
}
