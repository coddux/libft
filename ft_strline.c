#include "inc/libft.h"

int		ft_strline(char *str)
{
	int		i;
	int		lg;

	i = 0;
	lg = 0;
	while (str[i] != '\0')
		if (str[i] == '\n')
			lg = lg + 1;
	return (lg);
}
