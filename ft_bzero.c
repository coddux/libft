#include "includes/libft.h"

void	ft_bzero(void *v, size_t n)
{
	size_t	i;
	char	*sv;

	i = 0;
	sv = (char*)v;
	if (n > 0)
	{
		while (i < n)
		{
			sv[i] = '\0';
			i = i + 1;
		}
	}
}
